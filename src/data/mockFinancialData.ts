// Helper Functions
export function getDonationsByStatus(status: 'pending' | 'approved' | 'rejected'): Donation[] {
  return mockDonations.filter((d) => d.status === status);
}

export function getProjectWalletById(projectId: string): ProjectWallet | undefined {
  return mockProjectWallets.find((w) => w.projectId === projectId);
}

export function getTotalWalletBalance(): number {
  return mockProjectWallets.reduce((sum, wallet) => sum + wallet.balance, 0);
}

export function getPendingPaymentsCount(): number {
  return mockDonations.filter((d) => d.status === 'pending').length;
}

export function getPendingWithdrawalsCount(): number {
  return mockWithdrawalRequests.filter((w) => w.status === 'pending').length;
}
